    public Connection connection() {
        Connection con = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/StudentDB", "root", "");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            con = null;
        }
        return con;
    }

    public void fillTable() {
        try {
            Connection con = connection();
            Statement statement = con.createStatement();
            ResultSet result = statement.executeQuery("select * from students");
            DefaultTableModel model = new DefaultTableModel(new Object[]{"id", "name", "age", "grade"}, 0);
            while (result.next()) {
                int id = result.getInt("id");
                String username = result.getString("name");
                int age = result.getInt("age");
                String usertype = result.getString("grade");

                model.addRow(new Object[]{id, username, age, usertype});
            }
            jTable1.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    public void clearData() {
        Agetextfield.setText("");
        Nametextfield.setText("");
        Gradecombobox.setSelectedIndex(-1);

    }

