    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver");
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/StudentDB", "root", "")) {
                PreparedStatement st = con.prepareStatement("insert into students (id,name,age,grade) values ('" + Idtextfield.getText() + "','" + Nametextfield.getText() + "','" + Agetextfield.getText() + "','" + Gradecombobox.getSelectedItem() + "');");
                if (st.executeUpdate() > 0) {
                    JOptionPane.showConfirmDialog(null, "Student info added succesfully");
                    fillTable();
                    clearData();
                } else {
                    JOptionPane.showMessageDialog(null, "OOps something went wrong!");
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        {
            // TODO add your handling code here:
            if (Nametextfield.getText().equals("") || Agetextfield.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter all fields");
            } else {
                try {
                    Connection con = connection();
                    PreparedStatement stamt = con.prepareStatement("update students set name='" + Nametextfield.getText() + "',age='" + Agetextfield.getText() + "',grade='" + Gradecombobox.getSelectedItem().toString() + "' where id='" + Idtextfield.getText() + "'");
                    if (stamt.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(null, "user information updated successfully");
                        fillTable();
                        clearData();
                    } else {
                        JOptionPane.showMessageDialog(null, "Oops something went wrong!");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }

    }//GEN-LAST:event_btnupdateActionPerformed

